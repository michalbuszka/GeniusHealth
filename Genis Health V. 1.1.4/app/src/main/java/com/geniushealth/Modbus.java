    public static boolean checkSum (byte[] frame, int frameLength)
    {
        if (frame.length >= 3)
        {
            int[] crc =  calculateCRC(frame, 0, frameLength - 2);
            byte[] crcByte = new byte[2];
            crcByte[0] = (byte)crc[0];
            crcByte[1] = (byte)crc[1];
            if ((crcByte[0] == frame[frameLength - 2]) && (crcByte[1] == frame[frameLength - 1]))
            {
                return true;
            }
        }
        return false;
    }
    public static int[] calculateCRC(byte[] data, int offset, int len) {
        int[] crc = {0xFF, 0xFF};
        int nextByte;
        int uIndex; /* will index into CRC lookup*/ /* table */
        /* pass through message buffer */
        for (int i = offset; i < len && i < data.length; i++) {
            nextByte = 0xFF & ((int) data[i]);
            uIndex = crc[0] ^ nextByte; //*puchMsg++; /* calculate the CRC */
            crc[0] = crc[1] ^ auchCRCHi[uIndex];
            crc[1] = auchCRCLo[uIndex];
        }
        return crc;
    }
    public static byte[] int2Byte (int number)
    {
        byte[] returnBytes = new byte[2];
        returnBytes[0] = (byte)(number / 256);
        returnBytes[1] = (byte)(number % 256);
        return returnBytes;
    }
    public static byte[] prepareWriteManyRegistersFrame(int address, int startingAddress, int[] value)
    {
        byte[] frameByte = new byte[value.length * 2 + 9];
        frameByte[0] = (byte)address;
        frameByte[1] = (byte)Code.WRITE_N_REGISTERS;
        byte[] startingBytes = int2Byte(startingAddress);
        frameByte[2] = startingBytes[0];
        frameByte[3] = startingBytes[1];
        byte[] amountRegisters= int2Byte(value.length);
        frameByte[4] = amountRegisters[0];
        frameByte[5] = amountRegisters[1];
        frameByte[6] = (byte)(value.length * 2);
        int counter = 6;
        for (int i = 0; i < value.length; i++)
        {
            counter++;
            byte[] valueBytes = int2Byte(value[i]);
            frameByte[counter] = valueBytes[0];
            counter++;
            frameByte[counter] = valueBytes[1];
        }
        counter++;
        int[] crc = calculateCRC(frameByte, 0, counter);
        frameByte[counter] = (byte) crc[0];
        counter++;
        frameByte[counter] = (byte) crc[1];
        return frameByte;

    }
